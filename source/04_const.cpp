
#define day 7

// when defining a constant value,
// 1. use define
// 2. use const

int main()
{
    // 1. use define
    cout << "The total number of days in a week is: " << day << endl;

    // 2. use const
    const int month = 12;
    cout << "The total number of months in a year is: " << month << endl;

    system("pause");
    return 0;
}